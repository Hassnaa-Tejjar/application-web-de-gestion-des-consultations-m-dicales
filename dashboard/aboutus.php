                    <h1 id="apropos"><img src="images/ico_epingle.png" alt="Catégorie voyage" class="ico_categorie" />A propos de MaConsultationMedicale</h1>
                    <h4>MaConsultationMedicale ….C'est immédiat, simple et gratuit !</h4>
                    <p>MaConsultationMedicale est la première plateforme de gestion de rendez-vous médicaux en ligne en Afrique. MaConsultationMedicale change le quotidien des médecins et celui de leurs patients !  Il optimise la gestion des cabinets médicaux grâce à ses multiples fonctionnalités intégrées et permet aux patients de prendre rendez-vous en quelques clics 24h/7j gratuitement.
                   Fondé en 2014, MaConsultationMedicale a pour mission de simplifier le processus de recherche de médecins, garantir aux patients une meilleure expérience de prise de rendez-vous et permettre aux médecins une meilleure gestion de leurs rendez-vous.</p>
                   <h1 id="qf"><img src="images/ico_epingle.png" alt="Catégorie voyage" class="ico_categorie" />Questions fréquentes</h1>
                   <h4>Qu'est-ce que MaConsultationMedicale ?</h4>
                    <p>MaConsultationMedicale vous permet de trouver le médecin ou le dentiste qui vous convient, et de prendre rendez-vous directement en ligne.

                      MaConsultationMedicale réunit pour vous des informations fiables sur les praticiens autour de chez vous et vous évite de passer des heures au téléphone pour trouver celui qui pourra vous recevoir !</p>
                      <h4>Comment puis-je prendre un rendez-vous ?</h4>
                      <p>La procédure est très simple : sélectionnez la spécialité que vous souhaitez consulter, renseignez votre ville afin que MaConsultationMedicale vous propose les médecins les plus proches, enfin choisissez le médecin et le créneau qui vous conviennent. Ensuite, renseignez les champs demandés du formulaire, saisissez le code qui vous est envoyé par SMS, en un clic votre rendez-vous est pris !</p>
                      <h4>Comment savoir si mon rendez-vous est confirmé ?</h4>
                      <p>Lorsque votre demande rendez-vous est confirmée, vous recevez un email ainsi qu’un SMS de confirmation indiquant la date et l’heure de votre rendez-vous.</p>
                      <h4>J'ai besoin de décaler ou d'annuler mon rendez-vous. Comment faire ?</h4>
                      <p>Vous pouvez modifier ou annuler vos rendez-vous dans votre Espace personnel. Vous y retrouverez tous vos rendez-vous à venir dans la rubrique Mes Rendez-vous. En revanche, pensez à modifier ou à annuler vos rendez-vous suffisamment tôt pour que votre médecin puisse s'organiser !</p>
                       <h4>Comment choisir un médecin ou un dentiste ?</h4>
                       <p>C'est vous, et vous seul, qui pouvez décider. Nous vous fournissons le plus d'informations possible pour vous permettre de mieux connaître les praticiens autour de chez vous, mais nous ne choisissons en aucun cas à votre place !</p>
                       <h4>Prendre un rendez-vous avec MaConsultationMedicale, c'est gratuit ?</h4>
                       <p>Oui. C'est totalement gratuit : pas de piège, pas de numéro surtaxé, pas de frais cachés.</p>
                        <h4>Tous les médecins sont-ils sur MaConsultationMedicale ?</h4>
                        <p>Pas encore ! Vous pouvez retrouver sur notre Site tous les médecins ou les dentistes qui ont choisi d'adhérer à MaConsultationMedicale. Et ils sont de plus en plus nombreux à nous rejoindre pour vous permettre de vous simplifier la vie.</p>
                         <h4>Est-ce-que MaConsultationMedicale remplace les assistant(e)s ?</h4>
                         <p>Non, MaConsultationMedicale a pour objet d'aider les assistant(e)s et non de les remplacer.

On constate à l'usage que MaConsultationMedicale permet de réduire le nombre d'appels et les appels en attente, donc le stress lié au téléphone, de telle sorte que vos assistant(e)s se consacrent à des tâches plus valorisantes telles que la gestion du cabinet, l'accueil physique des patients et les soins.

De plus, les oublis et malentendus sont considérablement réduits grâce à MaConsultationMedicale!</p>
<h4>Quand MaConsultationMedicale sera-t-il disponible dans ma ville ?</h4>
<p>On y travaille ! Pour l'instant nous avons lancé  MaConsultationMedicale sur quelques villes (voir www. MaConsultationMedicale.com) mais très vite nous ouvrirons notre service dans d'autres villes.</p>

