              <h4> Pourquoi avons-nous besoin de certaines données personnelles ?</h4>
<p>MaConsultationMedicale est un Service qui permet à tout Utilisateur de trouver un Professionnel de Santé et de prendre rendez-vous en ligne avec l'un d'entre eux.</p>

<p>La collecte de certaines informations personnelles est nécessaire pour organiser la mise en relation de l'Utilisateur avec un Professionnel de Santé et permettre à ce dernier de recevoir son Patient dans les meilleures conditions. Par ailleurs, certaines de vos données personnelles sont stockées pour vous permettre de gérer vos rendez-vous, les modifier ou les annuler.</p>

<p>Ainsi, pour pouvoir accéder au Service, l'Utilisateur devra ouvrir un Compte personnel lors de la confirmation de sa demande de rendez-vous. La création du compte est subordonnée au choix d'un Nom d'utilisateur et d'un Mot de passe (ci-après les Identifiants).</p>

<h4>Quel type d'informations recueillons-nous?</h4>
<p>Dans le cadre de la mise en œuvre du Service, l'Utilisateur sera amené à renseigner des données personnelles le concernant. Parmi les données à caractère personnel de l'Utilisateur que la Société collecte auprès de lui peuvent figurer :</p>

<p>Ses nom et prénoms</p>
<p>Son adresse email</p>
<p>Son numéro de téléphone</p>
<p>Son age</p>
<p>Son adresse IP (adresse virtuelle de son ordinateur)</p>
<p>Notre Service peut inclure des enquêtes facultatives, destinées à recueillir votre opinion sur votre expérience sur notre Site, l'utilisation de notre Service, ou votre opinion sur les Professionnels de Sante que vous avez consultés.</p>

<p>Nous sommes également susceptibles de répertorier vos activités et préférences lorsque vous vous rendez sur notre Site (voir la rubrique "Cookies" ci-dessous).</p>

<h4>A quoi servent les informations que nous recueillons ?</h4>
<p>Les données personnelles collectées visent à permettre la mise en œuvre du Service de prise de rendez-vous en ligne. Ces données sont nécessaires au bon fonctionnement du Service et ne sont utilisées qu'aux fins suivantes :</p>

<p>Transmettre au Professionnel de Santé les informations nécessaires à l'organisation et la préparation du rendez-vous de l'Utilisateur</p>
<p>Permettre à l'Utilisateur de gérer à tout moment l'ensemble de ses rendez-vous via un Compte personnel sécurisé</p>
<p>Envoyer à l'Utilisateur des emails administratifs, par exemple pour vous prévenir de la confirmation d'un rendez-vous, de sa modification ou de son annulation, et vous rappeler les rendez-vous que vous avez programmés</p>
<p>Traiter les demandes reçues par le Service Client</p>
<p>Procéder à des analyses et recherches internes dans le but d'améliorer notre Site et notre Service</p>
<h4>Qui peut accéder aux informations que nous recueillons ?</h4>
<p>Certaines des informations personnelles collectées pourront être transmises au Professionnel de Santé auprès duquel vous avez effectué une demande de rendez-vous afin de lui permettre d'organiser le rendez-vous dans les meilleures conditions et de préparer sa consultation / son intervention. Chaque Professionnel de Santé destinataire d'informations personnelles dans le cadre du Service s'engage en y adhérant à garantir leur confidentialité, et ce dans le respect du secret professionnel auquel il est soumis. La Société s'engage à ne transmettre ces données à caractère personnel à aucun autre tiers non autorisé.</p>

