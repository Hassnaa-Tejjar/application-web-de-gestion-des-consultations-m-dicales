<p>Vous vous engagez à fournir des informations exactes quant à vos coordonnées personnelles ainsi que toute autre information nécessaire à l'accès aux Services du Site et à mettre à jour régulièrement ces informations si nécessaire.</p>

<p>Au Maroc, conformément à la loi n°09-08 promulguée par le Dahir 1-09-15 du 18 février 2009, relative à la protection des personnes physiques à l'égard du traitement des données à caractère personnel, vous bénéficiez d'un droit d'accès et de rectification aux informations qui vous concernent, que vous pouvez exercer en vous adressant à contact@dabadoc.com. Vous pouvez également, pour des motifs légitimes, vous opposer à ce que les données qui vous concernent fassent l'objet d'un traitement. Ce traitement a été notifié et autorisé par la CNDP au titre du récépissé / autorisation N° A-W-353/2017.</p>

<p>Les données personnelles collectées peuvent être transmises à AWS en Irlande conformément à la demande de transfert déposée auprès de la CNDP.</p>

